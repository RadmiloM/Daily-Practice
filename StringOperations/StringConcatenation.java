public class StringConcatenation {
    public static void main(String[] args) {
        String firstString = "Today is great ";
        String secondString = firstString + "day";
        System.out.println(secondString);
        String strFirst = "This is example with ";
        String strSecond = strFirst.concat("concatenation");
        System.out.println(strSecond);
    }

}
